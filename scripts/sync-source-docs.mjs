#!/usr/bin/env node
import fs from "node:fs/promises"
import path from "node:path"
import process from "node:process"

import { buildDocsNav } from "../quartz/util/docsNav.mjs"

const SOURCE_REPOSITORY = "self-evolving/repo"

const sourceRoot = path.resolve(process.argv[2] ?? "../sepo")
const contentRoot = path.resolve(process.argv[3] ?? "content")

// Keep source sections explicit so future content/blogs support can be added
// without reintroducing docs-specific markdown rewrites.
const contentSections = [
  {
    label: "docs",
    sourceDir: path.join(sourceRoot, ".agent", "docs"),
    destRel: "docs",
  },
]

function toPosix(filePath) {
  return filePath.split(path.sep).join("/")
}

function yamlString(value) {
  return JSON.stringify(value)
}

async function pathExists(filePath) {
  try {
    await fs.access(filePath)
    return true
  } catch {
    return false
  }
}

async function walk(dir) {
  const entries = await fs.readdir(dir, { withFileTypes: true })
  const files = []

  for (const entry of entries) {
    const absPath = path.join(dir, entry.name)
    if (entry.isDirectory()) {
      files.push(...(await walk(absPath)))
    } else if (entry.isFile()) {
      files.push(absPath)
    }
  }

  return files
}

function frontmatterMatch(markdown) {
  return markdown.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n?/)
}

function hasFrontmatterTitle(markdown) {
  const match = frontmatterMatch(markdown)
  return Boolean(match?.[1]?.match(/^title\s*:/m))
}

function bodyAfterFrontmatter(markdown) {
  const match = frontmatterMatch(markdown)
  return match ? markdown.slice(match[0].length) : markdown
}

async function validateMarkdownFile(filePath, section) {
  const markdown = await fs.readFile(filePath, "utf8")
  const rel = toPosix(path.relative(section.sourceDir, filePath))

  if (!hasFrontmatterTitle(markdown)) {
    throw new Error(`${section.label}/${rel} must define frontmatter with a title field`)
  }

  if (bodyAfterFrontmatter(markdown).trimStart().match(/^#\s+/)) {
    throw new Error(
      `${section.label}/${rel} starts with an H1 after frontmatter. ` +
        "Move the title into frontmatter so the docs layout does not render duplicate titles.",
    )
  }
}

async function validateContentSection(section) {
  if (!(await pathExists(section.sourceDir))) {
    throw new Error(`Could not find source ${section.label} directory at ${section.sourceDir}`)
  }

  const indexPath = path.join(section.sourceDir, "index.md")
  if (!(await pathExists(indexPath))) {
    throw new Error(
      `Expected ${section.label}/index.md in ${section.sourceDir}. ` +
        "Rename README.md files to index.md in the source docs before syncing.",
    )
  }

  const files = await walk(section.sourceDir)
  const readmes = files.filter((file) => path.basename(file).toLowerCase() === "readme.md")
  if (readmes.length > 0) {
    const list = readmes
      .map((file) => `${section.label}/${toPosix(path.relative(section.sourceDir, file))}`)
      .join("\n")
    throw new Error(
      `README.md files are not supported in synced ${section.label} content:\n${list}`,
    )
  }

  for (const file of files) {
    if (path.extname(file).toLowerCase() === ".md") {
      await validateMarkdownFile(file, section)
    }
  }

  buildDocsNav({ docsRoot: section.sourceDir, slugPrefix: section.destRel })

  return files
}

async function writeRootRedirect() {
  const sourceReadme = path.join(sourceRoot, "README.md")

  if (!(await pathExists(sourceReadme))) {
    throw new Error(`Could not find source README at ${sourceReadme}`)
  }

  const sourceRel = toPosix(path.relative(sourceRoot, sourceReadme))
  const generated = `<!-- Generated from ${SOURCE_REPOSITORY}:${sourceRel}. Do not edit this file by hand. -->`
  const frontmatter = [
    "---",
    `title: ${yamlString("Sepo Docs")}`,
    `source: ${yamlString(sourceRel)}`,
    `redirect: ${yamlString("docs/")}`,
    "---",
    "",
    generated,
    "",
  ].join("\n")
  const body = "Redirecting to [Sepo Docs](docs/)."

  await fs.mkdir(contentRoot, { recursive: true })
  await fs.writeFile(path.join(contentRoot, "index.md"), `${frontmatter}${body}\n`, "utf8")
}

async function copyContentSection(section, files) {
  for (const sourceFile of files) {
    const rel = toPosix(path.relative(section.sourceDir, sourceFile))
    const destFile = path.join(contentRoot, section.destRel, rel)
    await fs.mkdir(path.dirname(destFile), { recursive: true })
    await fs.copyFile(sourceFile, destFile)
  }
}

async function main() {
  await fs.rm(contentRoot, { recursive: true, force: true })
  await fs.mkdir(contentRoot, { recursive: true })

  await writeRootRedirect()

  for (const section of contentSections) {
    const files = await validateContentSection(section)
    await copyContentSection(section, files)
  }

  console.log(`Synced ${SOURCE_REPOSITORY} content from ${sourceRoot} into ${contentRoot}`)
}

main().catch((error) => {
  console.error(error)
  process.exit(1)
})
