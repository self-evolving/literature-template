#!/usr/bin/env node
import fs from "node:fs/promises"
import path from "node:path"
import process from "node:process"

const SOURCE_REPOSITORY = "self-evolving/repo"
const DEFAULT_SOURCE_REF = process.env.SOURCE_REF || "main"

const sourceRoot = path.resolve(process.argv[2] ?? "../sepo")
const contentRoot = path.resolve(process.argv[3] ?? "content")
const docsRoot = path.join(sourceRoot, ".agent", "docs")

function toPosix(filePath) {
  return filePath.split(path.sep).join("/")
}

function yamlString(value) {
  return JSON.stringify(value)
}

function isExternalLink(link) {
  return /^(?:[a-z][a-z0-9+.-]*:|#|\/)/i.test(link)
}

function splitLinkSuffix(link) {
  const hashIndex = link.indexOf("#")
  const queryIndex = link.indexOf("?")
  const suffixIndex = [hashIndex, queryIndex].filter((idx) => idx >= 0).sort((a, b) => a - b)[0]

  if (suffixIndex === undefined) {
    return { linkPath: link, suffix: "" }
  }

  return {
    linkPath: link.slice(0, suffixIndex),
    suffix: link.slice(suffixIndex),
  }
}

function isInside(parent, child) {
  const relative = path.relative(parent, child)
  return relative === "" || (!relative.startsWith("..") && !path.isAbsolute(relative))
}

function mapReadmeToIndex(relativePath) {
  const parts = toPosix(relativePath).split("/")
  if (parts.at(-1)?.toLowerCase() === "readme.md") {
    parts[parts.length - 1] = "index.md"
  }
  return parts.join("/")
}

function sourceBlobUrl(absPath) {
  const sourceRel = toPosix(path.relative(sourceRoot, absPath))
  return `https://github.com/${SOURCE_REPOSITORY}/blob/${DEFAULT_SOURCE_REF}/${sourceRel}`
}

function relativeSiteLink(fromDestRel, toDestRel) {
  const fromDir = path.posix.dirname(toPosix(fromDestRel))
  return path.posix.relative(fromDir, toPosix(toDestRel)) || "index.md"
}

function rewriteLink(link, sourceFile, destRel) {
  const trimmed = link.trim()
  if (!trimmed || isExternalLink(trimmed)) {
    return link
  }

  const { linkPath, suffix } = splitLinkSuffix(trimmed)
  if (!linkPath) {
    return link
  }

  const targetAbs = path.resolve(path.dirname(sourceFile), linkPath)
  const sourceReadme = path.join(sourceRoot, "README.md")

  if (targetAbs === sourceReadme) {
    return `${relativeSiteLink(destRel, "index.md")}${suffix}`
  }

  if (isInside(docsRoot, targetAbs)) {
    const docsRel = path.relative(docsRoot, targetAbs)
    const targetDestRel = toPosix(path.join("docs", mapReadmeToIndex(docsRel)))
    return `${relativeSiteLink(destRel, targetDestRel)}${suffix}`
  }

  if (isInside(sourceRoot, targetAbs)) {
    return `${sourceBlobUrl(targetAbs)}${suffix}`
  }

  return link
}

function rewriteMarkdownLinks(markdown, sourceFile, destRel) {
  return markdown.replace(/(!?\[[^\]\n]*\]\()([^\)\n]+)(\))/g, (_match, prefix, link, suffix) => {
    return `${prefix}${rewriteLink(link, sourceFile, destRel)}${suffix}`
  })
}

function extractFirstH1(markdown, fallbackTitle) {
  const h1Match = markdown.match(/^#\s+(.+?)\s*$/m)
  return h1Match?.[1]?.replace(/[`*_~]/g, "").trim() || fallbackTitle
}

function stripFirstH1(markdown) {
  return markdown.replace(/^#\s+.+?\s*\r?\n+/, "")
}

async function pathExists(filePath) {
  try {
    await fs.access(filePath)
    return true
  } catch {
    return false
  }
}

async function writeMarkdown(sourceFile, destRel, fallbackTitle) {
  const raw = await fs.readFile(sourceFile, "utf8")
  const title = extractFirstH1(raw, fallbackTitle)
  const sourceRel = toPosix(path.relative(sourceRoot, sourceFile))
  const body = rewriteMarkdownLinks(stripFirstH1(raw).trimStart(), sourceFile, destRel)
  const generated = `<!-- Generated from ${SOURCE_REPOSITORY}:${sourceRel}. Do not edit this file by hand. -->`
  const frontmatter = [
    "---",
    `title: ${yamlString(title)}`,
    `source: ${yamlString(sourceRel)}`,
    "---",
    "",
    generated,
    "",
  ].join("\n")

  await fs.mkdir(path.dirname(path.join(contentRoot, destRel)), { recursive: true })
  await fs.writeFile(path.join(contentRoot, destRel), `${frontmatter}${body.trimEnd()}\n`, "utf8")
}

async function writeRootRedirect() {
  const sourceReadme = path.join(sourceRoot, "README.md")
  const sourceRel = toPosix(path.relative(sourceRoot, sourceReadme))
  const generated = `<!-- Generated from ${SOURCE_REPOSITORY}:${sourceRel}. Do not edit this file by hand. -->`
  const frontmatter = [
    "---",
    `title: ${yamlString("Sepo Docs")}`,
    `source: ${yamlString(sourceRel)}`,
    `redirect: ${yamlString("docs/")}`,
    "---",
    "",
    generated,
    "",
  ].join("\n")
  const body = "Redirecting to [Sepo Docs](docs/index.md)."

  await fs.mkdir(contentRoot, { recursive: true })
  await fs.writeFile(path.join(contentRoot, "index.md"), `${frontmatter}${body}\n`, "utf8")
}

async function copyFile(sourceFile, destRel) {
  const destFile = path.join(contentRoot, destRel)
  await fs.mkdir(path.dirname(destFile), { recursive: true })
  await fs.copyFile(sourceFile, destFile)
}

async function walk(dir) {
  const entries = await fs.readdir(dir, { withFileTypes: true })
  const files = []

  for (const entry of entries) {
    const absPath = path.join(dir, entry.name)
    if (entry.isDirectory()) {
      files.push(...(await walk(absPath)))
    } else if (entry.isFile()) {
      files.push(absPath)
    }
  }

  return files
}

async function main() {
  const sourceReadme = path.join(sourceRoot, "README.md")

  if (!(await pathExists(sourceReadme))) {
    throw new Error(`Could not find source README at ${sourceReadme}`)
  }

  if (!(await pathExists(docsRoot))) {
    throw new Error(`Could not find source docs directory at ${docsRoot}`)
  }

  await fs.rm(contentRoot, { recursive: true, force: true })
  await fs.mkdir(contentRoot, { recursive: true })

  await writeRootRedirect()

  for (const sourceFile of await walk(docsRoot)) {
    const docsRel = path.relative(docsRoot, sourceFile)
    const ext = path.extname(sourceFile).toLowerCase()
    const destRel = toPosix(path.join("docs", ext === ".md" ? mapReadmeToIndex(docsRel) : docsRel))

    if (ext === ".md") {
      const fallbackTitle = path.basename(sourceFile, ext)
      await writeMarkdown(sourceFile, destRel, fallbackTitle)
    } else {
      await copyFile(sourceFile, destRel)
    }
  }

  console.log(`Synced ${SOURCE_REPOSITORY} docs from ${sourceRoot} into ${contentRoot}`)
}

main().catch((error) => {
  console.error(error)
  process.exit(1)
})
